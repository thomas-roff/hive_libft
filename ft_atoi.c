int	ft_atoi(const char *str)
{
	int	res;
	int	pn;
	int	i;

	res = 0;
	pn = 1;
	i = 0;
	while (str[i] < '0' || str[i] > '9')
	{
		if (str[i] == '-')
			pn = pn * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * pn);
}
